public class ExampleStrings {
    public static void main(String[] args) {
        String a = "\"30\" августа 1903г."; // относится к задачам "строки №2"
        String b = "C:\\Windows\\System32\\Drivers\\etc\\hosts"; // относится к задачам "строки №2"

        int c = 13 % 9; // относится к задачам "строки №3" и к заданию "Арифметика" в части оператора %

        System.out.println(a + System.lineSeparator() + b); // относится к задачам "строки №2"
        System.out.println(System.lineSeparator());
        System.out.println("Результат = " + c); // относится к задачам "строки №3" и к заданию "Арифметика" в части оператора %
    }
}
